<h4>3.	Ask your pediatrician for samples</h4>
<p>If you have plans to bottle feed, the cost of formula might have the changes for you going a major case of sticker shock. When you leave the hospital finally, stock up your diaper bag with as much formula as the nurses are willing to provide you. Always be prepared to ask‚Äîmost healthcare facilities won‚Äôt provide you unprompted. And don‚Äôt hesitate to request freebies from your pediatrician too. She can provide you with everything from formula to eczema cream.</p>
